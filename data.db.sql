	"timeOut"	INTEGER,
	FOREIGN KEY("employee") REFERENCES "employees"("id")
);
DROP TABLE IF EXISTS "employeeRole";
CREATE TABLE IF NOT EXISTS "employeeRole" (
	"id"	INTEGER PRIMARY KEY AUTOINCREMENT,
	"role"	TEXT
